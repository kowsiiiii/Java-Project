package org.oops;
//example2-encapsulation
public class Encapsulation_Ex2 {

	private void myMethod2() {
		System.out.println("hi, this is kowsalya");
	}

	private void myMethod3() {
		System.out.println("I would like to do something in java");
	}

	private void myMethod4() {
		System.out.println("also much more interesting in that");
	}

	public static void main(String[] args) {
		Encapsulation_Ex2 myobj2 = new Encapsulation_Ex2();
		myobj2.myMethod2();
		myobj2.myMethod3();
		myobj2.myMethod4();

	}

}
